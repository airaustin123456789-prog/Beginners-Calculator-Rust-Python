def main():

    print("")

    num1 = int(input("Choose your first number: "))

    print("")

    num2 = int(input("Choose the second number: "))

    print("")

    remainder = num1 % num2

    answer = num1 / num2

    print(f"{num1} / {num2} = {answer} with the remainder of {remainder}.")

    print("")

main()